// 1. Arithmetic Operation Script: Write a script that performs basic Arithmetic Operations(addition, subtraction, multiplication, devision, reminder) on two numbers and logs the results
const num1: number = 10;
const num2: number = 5;

let addition: number = num1 + num2;
console.log("Addition:", addition);

let subtraction: number = num1 - num2;
console.log("Subtraction:", subtraction);

let multiplication: number = num1 * num2;
console.log("Multiplication:", multiplication);

let division: number = num1 / num2;
console.log("Division:", division);

let remainder: number = num1 % num2;
console.log("Remainder:", remainder);
